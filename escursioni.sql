
DROP TYPE IF EXISTS enum_regione CASCADE;
DROP TYPE IF EXISTS enum_difficolta CASCADE;

DROP TABLE IF EXISTS PERSONA CASCADE;
DROP TABLE IF EXISTS TAPPA CASCADE;
DROP TABLE IF EXISTS PARCO CASCADE;
DROP TABLE IF EXISTS GUIDA CASCADE;
DROP TABLE IF EXISTS ESCURSIONISTA CASCADE;
DROP TABLE IF EXISTS RIFUGIO CASCADE;
DROP TABLE IF EXISTS CIMA CASCADE;
DROP TABLE IF EXISTS SENTIERO CASCADE;
DROP TABLE IF EXISTS attraversa CASCADE;
DROP TABLE IF EXISTS ESCURSIONE CASCADE;
DROP TABLE IF EXISTS partecipa CASCADE;

CREATE TYPE enum_regione AS ENUM ('Veneto', 'Lombardia', 'Trentino-Alto Adige', 'Friuli-Venezia Giulia');
CREATE TYPE enum_difficolta AS ENUM ('facile', 'medio', 'difficile', 'molto difficile');

CREATE TABLE PERSONA (
    cf CHAR(16) PRIMARY KEY,
    nome VARCHAR(100) NOT NULL,
    cognome VARCHAR(100) NOT NULL
);

CREATE TABLE TAPPA (
    id_tappa SERIAL PRIMARY KEY,
    nome VARCHAR(100) NOT NULL,
    altitudine_m INTEGER,
    provincia CHAR(2),
    copertura_rete_mobile BOOLEAN NOT NULL DEFAULT FALSE
);

CREATE TABLE PARCO (
    nome VARCHAR(100) NOT NULL,
    regione enum_regione NOT NULL,
    superficie_km2 DECIMAL(8,2),
    PRIMARY KEY (nome, regione)
);

CREATE TABLE GUIDA (
    cf CHAR(16) PRIMARY KEY REFERENCES PERSONA(cf),
    num_tesserino VARCHAR(50) UNIQUE NOT NULL
);

CREATE TABLE ESCURSIONISTA (
    cf CHAR(16) PRIMARY KEY REFERENCES PERSONA(cf),
    scadenza_certificato_medico DATE NOT NULL
);

CREATE TABLE RIFUGIO (
    id_tappa INT PRIMARY KEY,
    posti_letto SMALLINT NOT NULL DEFAULT 0,
    cf_gestore CHAR(16),
    proprieta VARCHAR(100),
    FOREIGN KEY (id_tappa) REFERENCES TAPPA(id_tappa) ON DELETE CASCADE,
    FOREIGN KEY (cf_gestore) REFERENCES PERSONA(cf)
);

CREATE TABLE CIMA (
    id_tappa INT PRIMARY KEY,
    gruppo_montuoso VARCHAR(100) NOT NULL,
    FOREIGN KEY (id_tappa) REFERENCES TAPPA(id_tappa) ON DELETE CASCADE
);

CREATE TABLE SENTIERO (
    codice INTEGER PRIMARY KEY,
    nome VARCHAR(100) NOT NULL,
    difficolta enum_difficolta,
    lunghezza_km DECIMAL(5,2),
    tempo_percorrenza INTEGER,
    dislivello INTEGER,
    num_tappe INTEGER,
    nome_parco VARCHAR(100),
    regione_parco enum_regione,
    id_tappa_partenza INTEGER NOT NULL REFERENCES TAPPA(id_tappa),
    id_tappa_arrivo INTEGER NOT NULL REFERENCES TAPPA(id_tappa),
    FOREIGN KEY (nome_parco, regione_parco) REFERENCES PARCO(nome, regione)
);

CREATE TABLE attraversa (
    codice_sentiero INTEGER REFERENCES SENTIERO(codice),
    id_tappa INTEGER REFERENCES TAPPA(id_tappa),
    PRIMARY KEY (codice_sentiero, id_tappa)
);

CREATE TABLE ESCURSIONE (
    id_escursione SERIAL PRIMARY KEY,
    data_programmata DATE NOT NULL,
    costo DECIMAL(8,2),
    codice_sentiero INTEGER NOT NULL REFERENCES SENTIERO(codice),
    posti_disponibili INTEGER,
    cf_guida CHAR(16) NOT NULL REFERENCES GUIDA(cf),
    UNIQUE(data_programmata, codice_sentiero),
    UNIQUE(data_programmata, cf_guida)
);

CREATE TABLE partecipa (
    cf_escursionista CHAR(16) REFERENCES ESCURSIONISTA(cf),
    id_escursione INTEGER REFERENCES ESCURSIONE(id_escursione),
    PRIMARY KEY (cf_escursionista, id_escursione)
);

DROP INDEX IF EXISTS idx_escursione_data;

CREATE INDEX idx_escursione_data ON ESCURSIONE(data_programmata);

